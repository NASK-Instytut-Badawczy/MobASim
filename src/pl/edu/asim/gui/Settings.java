package pl.edu.asim.gui;

public class Settings {

	private String defaultModelerName;
	private String defaultModelerType;

	public void setDefaultModelerName(String defaultModelerName) {
		this.defaultModelerName = defaultModelerName;
	}

	public String getDefaultModelerName() {
		return defaultModelerName;
	}

	public void setDefaultModelerType(String defaultModelerType) {
		this.defaultModelerType = defaultModelerType;
	}

	public String getDefaultModelerType() {
		return defaultModelerType;
	}
	
}
